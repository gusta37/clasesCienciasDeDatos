""" Condicional completo (if - else) con expresión lógica simple

Desarrollo un programa que verificar si un número es mayor a 10. Si es así, imprimirá "El número es mayor a 10", de lo contrario, imprimirá "El número es menor que 10". """

numero = int(input("Ingrese un número: "))

if numero > 10:
    print("El número es mayor a 10")
else:
    print("El número es menor que 10")